# Add
fruits = ["apple", "banana", "orange"]
fruits.insert(1, "grape")
print("Add (insert):", fruits)

# Append
numbers = [1, 2, 3]
numbers.append(4)
print("Append:", numbers)

# Extend
list1 = [1, 2, 3]
list2 = [4, 5, 6]
list1.extend(list2)
print("Extend:", list1)

# Delete
fruits = ["apple", "banana", "orange"]
del fruits[1]
print("Delete (del):", fruits)

